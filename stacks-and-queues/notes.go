/*
Stacks and Queues are linear data structures
They have flexible sizes i.e. they grow as you add data and shrink as you remove data

STACKS
Stacks have a Last In First Out (LIFO) data structure - The last one you put in is the first
one you take out. Just llke a stack of books.

- push -> add data to a stack
- pop -> remove data from a stack

QUEUES
First in First out data (FIFO) structure - FIrst data in is the first data out e.g a queue
at a cashier.

- Enqueue -> add data to a queue
- Dequeue -> Remove data from a queue
*/
